// Using createSlice form Redux Toolkit
import { createSlice } from '@reduxjs/toolkit';

// initialState value starts at 0. Increment, decrement, increseByAmount reduers modify the value property of the state.
const initialState = {
  value: 0,
};

const counterSlice = createSlice({
  name: 'counter',
  initialState,
  reducers: {
    increment: (state) => {
      state.value++;
    },
    decrement: (state) => {
      state.value--;
    },
    incrementByAmount: (state, action) => {
      state.value += action.payload;
    },
  },
});

export const { increment, decrement, incrementByAmount } = counterSlice.actions;
export default counterSlice.reducer;
